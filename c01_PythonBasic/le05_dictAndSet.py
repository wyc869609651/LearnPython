# dict
# Python内置了字典： dict的支持，dict全称dictionary，在其他语言中也称为map,
# 使用（key-value）存储，具有极快的存储速度

scoreDict = {'Michael': 95, 'Bob': 75, 'Tracy': 85}
print(scoreDict['Michael'])
scoreDict['Adam'] = 67
print(scoreDict['Adam'])
print(scoreDict)

